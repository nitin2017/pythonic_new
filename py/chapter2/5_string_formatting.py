name = "Nitin"
age = 28

# STRING FORMATTING in PYTHON 3
print("hello {} your age is {}".format(name,age)) # {} is placeholder

# STRING FORMATTING IN PYTHON 3.6 (BEST AND CLEAN)
print(f"hello {name} your age is {age}")
print(f"hello {name} your age is {age-2}") # CALCULATIONS ALONG WITH FORMATTING
