lang = "python"

# syntax - [start_srguement : stop_arguement : step_arguement]

print(lang[0:5]) # pytho
print(lang[0:5:1]) # pytho
print(lang[0:5:2]) # pto skipping every next character
print(lang[0::3]) # ph

print(lang[5::-1]) # nohtyp --- it reversed the string
print(lang[::-1]) # nohtyp
print(lang[-1::-1]) # nohtyp


