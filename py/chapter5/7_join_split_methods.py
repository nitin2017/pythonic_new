# split method 
# convert string to list
# Now you want to break this string with spaces and store the elements in a list 
user_info ='Nitin 24'.split()
print(user_info) # ['Nitin', '24']

# join method 
# convert list to string

user_info = ['Nitin','24'] # here 24 is string
print(','.join(user_info)) # Nitin,24 